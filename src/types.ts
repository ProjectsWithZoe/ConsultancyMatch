export type ExperienceLevel = 'entry' | 'junior' | 'intermediate' | 'midlevel' | 'senior' | 'expert';
export type ConsultantCategory = 'Technology' | 'Strategy' | 'Operations' | 'Finance' | 'HR' | 'Marketing';
export type LocationType = 'Remote' | 'On-site' | 'Hybrid';

export interface SkillExperience {
  skill: string;
  yearsOfExperience: number;
}

export interface Project {
  title: string;
  description: string;
  duration: string;
  skills: string[];
  category: ConsultantCategory;
}

export interface Consultant {
  id: string;
  name: string;
  category: ConsultantCategory;
  skills: SkillExperience[];
  availability: 'full-time' | 'part-time' | 'contract';
  location: LocationType;
  country: string;
  hourlyRate: number;
  previousProjects: Project[];
  rating: number;
}

export interface ConsultingFirm {
  id: string;
  name: string;
  category: ConsultantCategory;
  requiredSkills: {
    skill: string;
    minimumExperience: ExperienceLevel;
  }[];
  projectDuration: string;
  estimatedHours?: number;
  minimumHourlyRate: number;
  location: LocationType;
  country: string;
  budget: number;
}

export const getExperienceLevel = (years: number): ExperienceLevel => {
  if (years <= 1) return 'entry';
  if (years <= 2) return 'junior';
  if (years <= 4) return 'intermediate';
  if (years <= 6) return 'midlevel';
  if (years <= 8) return 'senior';
  return 'expert';
};

